    private void viewVitalSignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewVitalSignBtnActionPerformed
    int selectedRow = VitalSignTable.getSelectedRow();
    if(selectedRow>=0){
        VitalSign vs=(VitalSign)VitalSignTable.getValueAt(selectedRow,0);
        bloodPressureTxtField.setText(String.valueOf(vs.getBloodPressure()));
        pulseTxtField.setText(String.valueOf(vs.getPulse()));
        tempTxtField.setText(String.valueOf(vs.getTemp()));
    }
    else{
        JOptionPane.showMessageDialog(null, "Please Select a Row from the Table first","Warning",JOptionPane.WARNING_MESSAGE    );
    }
    }//GEN-LAST:event_viewVitalSignBtnActionPerformed

