    private void populateTable() {
    DefaultTableModel dtm = (DefaultTableModel)VitalSignTable.getModel();    
    dtm.setRowCount(0);
    for(VitalSign vs : vitalSignHistory.getVitalSignHistory())
    {
        Object row[] = new Object[1];
        row[0]=vs;
        dtm.addRow(row);
    }
    }

    private void resetFields() {
        bloodPressureTxtField.setText("");
        pulseTxtField.setText("");
        tempTxtField.setText("");
    }
