    private void populateTopProduct() {
        ArrayList<OrderItem> oItem=new ArrayList();
        for(Order o:moc.getOrderCatalog())
        {
        
        for(OrderItem oi:o.getOrderItemList())
        {
           
            Collections.sort(o.getOrderItemList());
            
            oItem.add(o.getOrderItemList().get(0));
           
        break;
        }
        
        }
      Collections.sort(oItem);
        
        mostPopularPrdTXTField.setText(String.valueOf(oItem.get(0).getProduct()));
        
                  
    
    } 

 

