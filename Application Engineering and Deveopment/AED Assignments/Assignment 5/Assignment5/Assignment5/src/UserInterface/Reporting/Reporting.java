    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    double commission = 0.0;
        
        SalesPerson salesperson = (SalesPerson) salespersonCombo.getSelectedItem();
        int commAboveTarget = 0;
        int commBelowTarget = 0;
        for(Order o : moc.getOrderCatalog())
        {
        int size = o.getOrderItemList().size();
        
        for(int i=0;i<size;i++)
        {  
            OrderItem items = (OrderItem) o.getOrderItemList().get(i);
            if(o.getSalesPerson().equals(salesperson))
            {
                int salesPrice = o.fetchOrder(i);
                int target = items.getProduct().getTargetPrice();
                int quantity = items.getQuantity();
            
                if(salesPrice<=target)
                {
                    commission = commission+(salesPrice*0.10*quantity);
                    ++commBelowTarget;
                }
                else
                {
                    commission = commission+(salesPrice*0.15*quantity);
                    ++commAboveTarget;
                    
                }

            }   
          }
        }
        commissionTxtBox.setText(String.valueOf(commission));
        belowTarget.setText(String.valueOf(commBelowTarget));
        aboveTarget.setText(String.valueOf(commAboveTarget));
    }//GEN-LAST:event_jButton1ActionPerformed


