    private void populateTable() {
    Inventory inv = new Inventory();
    DefaultTableModel dtm = (DefaultTableModel)inventoryTablee.getModel();
    dtm.setRowCount(0);
    for(Drug drug : cvsStore.getDrugCatalog().getDrugList()){
        Object row [] = new Object[11];
        row[0] = drug.getDrugName();
        row[1] = drug.getDrugSerialNumber();
        row[2] = drug.getDrugMnufacturer();
        row[3] = drug.getDosageForm();
        row[4] = drug.getWeight();
        row[5] = drug.getDateManufactured();
        row[6] = drug.getExpirationDate();
        row[7] = drug.getPatientFactor();
        row[8] = drug.getListPrice();
        row[9] = drug.getQuantity();
        dtm.addRow(row);
    }
    }
