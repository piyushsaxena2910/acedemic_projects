    private void submitCarBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitCarBTNActionPerformed
        FieldLevelValidation flv = new FieldLevelValidation();
        if(baseFarePerHourTXTField.getText().equals("") || hoursForRaiseTXTField.getText().equals("") 
               || raisedFarePerHourTXTField.getText().equals("")){
               
            JOptionPane.showMessageDialog(null, "One or More Fields are Empty");
        }
        
        else if(baseFarePerHourTXTField.getText().equals("0") 
                || hoursForRaiseTXTField.getText().equals("0") 
               || raisedFarePerHourTXTField.getText().equals("0")){
            JOptionPane.showMessageDialog(null, "Cannot enter a 0 in Base Fare, Raised Fare or Raise Fare From Hours field");
        }
        else if(!flv.validateIntegerField(baseFarePerHourTXTField.getText(), 2)){
            JOptionPane.showMessageDialog(null, "Invalid Base Fare Per Hour");
        }
        
        else if(!flv.validateIntegerField(hoursForRaiseTXTField.getText(), 2)){
            JOptionPane.showMessageDialog(null, "Invalid Hours For Fare Raise");
        }
        
        else if(!flv.validateIntegerField(raisedFarePerHourTXTField.getText(), 2)){
            JOptionPane.showMessageDialog(null, "Invalid Raised Fare Per Hour");
        }
        
        else if((Integer.parseInt(hoursForRaiseTXTField.getText()))>8){
            JOptionPane.showMessageDialog(null, "You cannot chose a number above 8");
        }
        else{
        CarOwner carOwner = (CarOwner)userAccount.getTraveller();
        
        if(carOwner.getBasePricePerHour()==0){

            carOwner.setBasePricePerHour(Integer.parseInt(baseFarePerHourTXTField.getText()));
            carOwner.setRaisedPricePerHour(Integer.parseInt(raisedFarePerHourTXTField.getText()));
            carOwner.setRaisePriceAferHours(Integer.parseInt(hoursForRaiseTXTField.getText()));
            JOptionPane.showMessageDialog(null, "Successfully Saved!");
        }

        else{
            
            baseFarePerHourTXTField.setText(String.valueOf(carOwner.getBasePricePerHour()));
            raisedFarePerHourTXTField.setText(String.valueOf(carOwner.getRaisedPricePerHour()));
            hoursForRaiseTXTField.setText(String.valueOf(carOwner.getRaisePriceAferHours()));
            JOptionPane.showMessageDialog(null, "Successfully Saved!");
        }
        }
    }//GEN-LAST:event_submitCarBTNActionPerformed


